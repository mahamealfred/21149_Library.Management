    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Account ac=new Account();
        AccountDao dao=new AccountDao();
        HiddenPassword h=new HiddenPassword();
        if(passtxt.getText().equals(cpasstxt.getText())){
            ac.setFirstname(firsttxt.getText());
            ac.setLastname(lasttxt.getText());
            ac.setUsername(usertxt.getText());
            ac.setEmail(emailtxt.getText());
            ac.setPassword(h.encodePassword(passtxt.getText()));
            dao.createAccount(ac);
            JOptionPane.showMessageDialog(this,"account created susseful!!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

